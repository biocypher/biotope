"""Write a BioCypher project to disk from compiled semantic mappings.

Layout::

    project_dir/
        config/
            biocypher_config.yaml
            schema_config.yaml
        mappings/
            <one copy of each mapping.yaml>
        generated/
            <mapping_stem>/
                __init__.py
                adapter.py
        alignment.yaml    # if provided
        create_knowledge_graph.py

* ``schema_config.yaml`` is generated from the semantic IR (no manual
  authoring). Entities emit ``namespace`` (never ``preferred_id``), and
  ``input_label`` is the mapping key so the adapter↔schema contract is
  autogenerated.
* ``generated/<mapping_stem>/adapter.py`` is a deterministic, inspectable
  Python module that wraps the compiled adapter. Users never edit it; it
  exists for provenance and to make builds debuggable.
* The build is strict: unresolved or legacy mappings are rejected.
"""

from __future__ import annotations

import shutil
from pathlib import Path
from typing import Any

import yaml

from biotope.croissant.alignment.loader import load_alignment
from biotope.croissant.mapping.compile import derive_namespace, derive_schema_term
from biotope.croissant.mapping.loader import load_mapping
from biotope.croissant.mapping.model import (
    Mapping,
    Selector,
)
from biotope.croissant.spec import FieldKind, load_from_path


SCHEMA_CONFIG_HEADER = (
    "# Generated by `biotope build` from semantic mapping files.\n"
    "# Edit the source mapping(s) instead — this file is overwritten on every build.\n"
    "# See https://biocypher.org/BioCypher/schema-config.html for the BioCypher spec.\n"
)


def _emit_schema_config(mappings: list[Mapping]) -> str:
    """Build ``schema_config.yaml`` from a list of compiled :class:`Mapping`."""
    config: dict[str, dict[str, Any]] = {}
    entity_terms: dict[tuple[int, str], str] = {}
    seen_terms: set[str] = set()

    for index, mapping in enumerate(mappings):
        for name, entity in mapping.entities.items():
            schema_term = derive_schema_term(name, entity.schema_term)
            entity_terms[(index, name)] = schema_term
            if schema_term in seen_terms:
                continue
            seen_terms.add(schema_term)
            config[schema_term] = {
                "represented_as": "node",
                "input_label": name,
                "namespace": derive_namespace(mapping, entity),
                "properties": _typed_properties(mapping, entity.record_set, entity.properties),
            }

    for index, mapping in enumerate(mappings):
        for name, relation in mapping.relations.items():
            schema_term = derive_schema_term(name, relation.schema_term)
            if schema_term in seen_terms:
                continue
            seen_terms.add(schema_term)
            source_key = relation.source.entity if relation.source else None
            target_key = relation.target.entity if relation.target else None
            source_term = entity_terms.get((index, source_key), source_key or "")
            target_term = entity_terms.get((index, target_key), target_key or "")
            config[schema_term] = {
                "represented_as": "edge",
                "input_label": name,
                "source": source_term,
                "target": target_term,
                "properties": _typed_properties(mapping, relation.record_set, relation.properties),
            }

    return SCHEMA_CONFIG_HEADER + yaml.safe_dump(config, sort_keys=False)


def _typed_properties(
    mapping: Mapping,
    record_set_name: str | None,
    properties: dict[str, Selector],
) -> dict[str, str]:
    """Map property names to BioCypher type strings, inferred from Croissant fields."""
    if not properties:
        return {}
    if record_set_name is None:
        return {prop: "str" for prop in properties}

    try:
        dataset = load_from_path(mapping.croissant)
    except Exception:
        return {prop: "str" for prop in properties}
    rs = dataset.record_set_by_name(record_set_name)
    if rs is None:
        return {prop: "str" for prop in properties}

    field_kinds: dict[str, FieldKind] = {}
    for f in rs.field:
        try:
            field_kinds[f.name] = f.kind()
        except ValueError:
            continue

    out: dict[str, str] = {}
    for prop_name, selector in properties.items():
        out[prop_name] = _selector_type(selector, mapping, field_kinds)
    return out


def _selector_type(
    selector: Selector,
    mapping: Mapping,
    field_kinds: dict[str, FieldKind],
) -> str:
    if selector.use is not None:
        return "str"
    if selector.transform == "as_curie":
        return "str"
    if selector.field is None or selector.field.startswith("$item"):
        return "str"
    kind = field_kinds.get(selector.field)
    if kind is None:
        return "str"
    if kind == FieldKind.STRUCT:
        msg = f"Struct-valued property field {selector.field!r} is not supported in v1"
        raise ValueError(msg)
    return _BIOCYPHER_TYPES.get(kind, "str")


_BIOCYPHER_TYPES: dict[FieldKind, str] = {
    FieldKind.STRING: "str",
    FieldKind.URL: "str",
    FieldKind.DATE: "str",
    FieldKind.BOOLEAN: "bool",
    FieldKind.INTEGER: "int",
    FieldKind.FLOAT: "float",
    FieldKind.ARRAY: "str[]",
}


