def _emit_build_script(project_name: str, stems: list[str], aligned: bool) -> str:
    """Build the ``create_knowledge_graph.py`` entry script."""
    imports = "\n".join(f"from generated.{stem}.adapter import build_adapter as _build_{stem}" for stem in stems)
    adapter_inits = ",\n        ".join(f'"{stem}": _build_{stem}()' for stem in stems)
    if aligned:
        alignment_import = "from biotope.croissant.alignment import load_alignment, merge_adapters\n"
        write_block = (
            "    alignment = load_alignment(HERE / 'alignment.yaml')\n"
            "    merged = merge_adapters(adapters, alignment)\n"
            "    bc.write_nodes(merged.get_nodes())\n"
            "    bc.write_edges(merged.get_edges())\n"
        )
        itertools_import = ""
    else:
        alignment_import = ""
        itertools_import = "from itertools import chain\n"
        write_block = (
            "    bc.write_nodes(chain.from_iterable(a.get_nodes() for a in adapters.values()))\n"
            "    bc.write_edges(chain.from_iterable(a.get_edges() for a in adapters.values()))\n"
        )
    return f'''"""Generated entry point for the {project_name} graph build."""

import os
from pathlib import Path
import sys
{itertools_import}
from biocypher import BioCypher

HERE = Path(__file__).parent
if str(HERE) not in sys.path:
    sys.path.insert(0, str(HERE))

# Run from build/ so BioCypher's relative `output_directory: biocypher-out`
# (configured in config/biocypher_config.yaml) lands inside this build,
# where `biotope view` looks for it. Adapters resolve their own paths via
# __file__ so this chdir does not affect mapping/croissant lookup.
os.chdir(HERE)

{imports}
{alignment_import}

def build() -> None:
    bc = BioCypher(
        biocypher_config_path=str(HERE / "config" / "biocypher_config.yaml"),
        schema_config_path=str(HERE / "config" / "schema_config.yaml"),
    )

    adapters = {{
        {adapter_inits}
    }}

{write_block}    bc.write_import_call()


if __name__ == "__main__":
    build()
'''


def materialize_project(
    project_dir: Path,
    mapping_paths: list[Path],
    alignment_path: Path | None = None,
) -> dict[str, str]:
    """Write a runnable BioCypher project to ``project_dir``.

    Strict: unresolved or legacy mappings raise ``ValueError``.
    """
    project_dir.mkdir(parents=True, exist_ok=True)
    (project_dir / "config").mkdir(exist_ok=True)
    (project_dir / "mappings").mkdir(exist_ok=True)
    (project_dir / "generated").mkdir(exist_ok=True)
    (project_dir / "generated" / "__init__.py").write_text(
        '"""Generated adapters for biotope build (do not edit)."""\n'
    )

    mappings = [load_mapping(p) for p in mapping_paths]
    for mapping, path in zip(mappings, mapping_paths, strict=False):
        try:
            mapping.assert_resolved()
        except ValueError as exc:
            msg = f"Mapping {path} is not fully resolved: {exc}"
            raise ValueError(msg) from exc

    schema_config_path = project_dir / "config" / "schema_config.yaml"
    schema_config_path.write_text(_emit_schema_config(mappings))

    biocypher_config_path = project_dir / "config" / "biocypher_config.yaml"
    if not biocypher_config_path.exists():
        # `head_ontology: null` opts into BioCypher's headless mode: no remote
        # Biolink retrieval at build time. The per-build class hierarchy is
        # defined exclusively by `schema_config.yaml`, which biotope
        # regenerates deterministically from the resolved mappings on every
        # run. Schema evolution happens between builds via `project.yaml`
        # + `biotope map`, never within a build. Requires biocypher with the
        # NullOntology shim; older versions ignore the key and will still
        # fetch Biolink — upgrade if you see network errors on first run.
        biocypher_config_path.write_text(
            "biocypher:\n"
            "  dbms: csv\n"
            "  log_to_disk: true\n"
            "  output_directory: biocypher-out\n"
            "  head_ontology: null\n",
        )

    stems: list[str] = []
    for src in mapping_paths:
        stem = _mapping_stem(src)
        stems.append(stem)
        shutil.copy(src, project_dir / "mappings" / src.name)
        pkg_dir = project_dir / "generated" / stem
        pkg_dir.mkdir(exist_ok=True)
        adapter_code, init_code = _emit_generated_package(stem, src.name)
        (pkg_dir / "adapter.py").write_text(adapter_code)
        (pkg_dir / "__init__.py").write_text(init_code)

    written_alignment: Path | None = None
    if alignment_path is not None:
        load_alignment(alignment_path)  # validate
        written_alignment = project_dir / "alignment.yaml"
        shutil.copy(alignment_path, written_alignment)

    script_path = project_dir / "create_knowledge_graph.py"
    script_path.write_text(
        _emit_build_script(
            project_name=project_dir.name,
            stems=stems,
            aligned=written_alignment is not None,
        ),
    )

    return {
        "project_dir": str(project_dir),
        "schema_config": str(schema_config_path),
        "biocypher_config": str(biocypher_config_path),
        "build_script": str(script_path),
        "alignment": str(written_alignment) if written_alignment else "",
        "generated_packages": ",".join(stems),
    }


def _mapping_stem(path: Path) -> str:
    name = path.name
    for suffix in (".mapping.yaml", ".mapping.yml", ".yaml", ".yml"):
        if name.endswith(suffix):
            return name[: -len(suffix)]
    return path.stem
